from aes.mixColumns import mixColumns, invMixColumns, shiftRows, invShiftRows
"""
This file tests the mixColumns functions
to run any of the test you must install run the command
python pip install pytest in the terminal. Then you can run the command
python -m pytest -v 
"""


def test_mixColumns():
    state = [
        [0xdb, 0x00, 0x00, 0x00],
        [0x13, 0x00, 0x00, 0x00],
        [0x53, 0x00, 0x00, 0x00],
        [0x45, 0x00, 0x00, 0x00]
    ]

    result = mixColumns(state)

    assert result[0][0] == 0x8e
    assert result[1][0] == 0x4d
    assert result[2][0] == 0xa1
    assert result[3][0] == 0xbc


def test_invMixColumns():
    state = [
        [0xdb, 0x00, 0x00, 0x00],
        [0x13, 0x00, 0x00, 0x00],
        [0x53, 0x00, 0x00, 0x00],
        [0x45, 0x00, 0x00, 0x00]
    ]

    mixed = mixColumns(state)
    restored = invMixColumns(mixed)

    assert restored == state


def test_shiftRows():
    state = [
        [0x00, 0x01, 0x02, 0x03],
        [0x10, 0x11, 0x12, 0x13],
        [0x20, 0x21, 0x22, 0x23],
        [0x30, 0x31, 0x32, 0x33]
    ]

    result = shiftRows(state)

    assert result == [
        [0x00, 0x01, 0x02, 0x03],
        [0x11, 0x12, 0x13, 0x10],
        [0x22, 0x23, 0x20, 0x21],
        [0x33, 0x30, 0x31, 0x32]
    ]


def test_invShiftRows():
    state = [
        [0x00, 0x01, 0x02, 0x03],
        [0x10, 0x11, 0x12, 0x13],
        [0x20, 0x21, 0x22, 0x23],
        [0x30, 0x31, 0x32, 0x33]
    ]

    shifted = shiftRows(state)
    restored = invShiftRows(shifted)

    assert restored == state