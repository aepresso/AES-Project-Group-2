def test_addRoundKey():
    """
    Testins addRoundKey functions to 
    check that it does xor the bytes correctly
    """
    testStateBox = [ 
        [0x01, 0x01,0x01,0x01],
        [0x01,0x01,0x01,0x01],
        [0x01,0x01,0x01,0x01],
        [0x01,0x01,0x01,0x01]
    ]
    testRoundKey = [
        [0xF1, 0xF1,0xF1,0xF1],
        [0xF1,0xF1,0xF1,0xF1],
        [0xF1,0xF1,0xF1,0xF1],
        [0xF1,0xF1,0xF1,0xF1]
    ]
    assert addRoundKey(testStateBox, testRoundKey) == [
        [0xF0,0xF0,0xF0,0xF0],
        [0xF0,0xF0,0xF0,0xF0],
        [0xF0,0xF0,0xF0,0xF0],
        [0xF0,0xF0,0xF0,0xF0]
    ]

def test_subBytes():
    """
    Tesing subBytes on a 4x4 matrix and making sure it works properly
    """
    testSimpleMatrix = [
        [0x00,0x00,0x00,0x00],
        [0xFF,0xFF,0xFF,0xFF],
        [0x00,0x00,0x00,0x00],
        [0xFF,0xFF,0xFF,0xFF]
    ]
    assert subBytes(testSimpleMatrix) == [
        [0x63,0x63,0x63,0x63],
        [0x16,0x16,0x16,0x16],
        [0x63,0x63,0x63,0x63],
        [0x16,0x16,0x16,0x16]
    ]

