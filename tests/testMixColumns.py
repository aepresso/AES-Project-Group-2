from aes.MixColumns import MixColumns

def testMix_columns():
    state = [
        [0xdb, 0x13, 0x53, 0x45],
        [0xf2, 0x0a, 0x22, 0x5c],
        [0x01, 0x01, 0x01, 0x01],
        [0xc6, 0xc6, 0xc6, 0xc6]
    ]

    result = MixColumns(state)

    assert result[0][0] == 0x8e
    assert result[1][0] == 0x4d
    assert result[2][0] == 0xa1
    assert result[3][0] == 0xbc