from aes.sBox import *
"""
Testing all functions in sBox to confirm that they work
"""
def test_subByte():
    """
    Testing and comparing in sBox table to ensure that 
    the subByte functions is working properly
    """
    assert subByte(0x00) == 0x63
    assert subByte(0x41) == 0x83
    assert subByte(0xF3) == 0x0D
    assert subByte(0x34) != 0x01

