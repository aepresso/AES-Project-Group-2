"""
Tests with base key and round count from lecture slides with key and round count 9. Another with the same key and round count 2 which I verified is correct.
Final test uses a different key and round count 9 from the lecture slides. 
"""
def test_keyExpansion():
    key = [[0xEA, 0xD2, 0x73, 0x21], [0xB5, 0x8D, 0xBA, 0xD2], [0x31, 0x2B, 0xF5, 0x60], [0x7F, 0x8D, 0x29, 0x2F]]
    result = keyExpansion(key, 9)
    assert result == [[0xAC, 0x77, 0x66, 0xF3], [0x19, 0xFA, 0xDC, 0x21], [0x28, 0xD1, 0x29, 0x41], [0x57, 0x5C, 0x00, 0x6E]]
    result2 = keyExpansion(key, 2)
    assert result2 == [[0xB5, 0x77, 0x66, 0xF3], [0x00, 0xFA, 0xDC, 0x21], [0x31, 0xD1, 0x29, 0x41], [0x4E, 0x5C, 0x00, 0x6E]]
    key2 = [[0x48, 0x26, 0x45, 0x20], [0xF3, 0x1B, 0xA2, 0xD7], [0xCB, 0xC3, 0xAA, 0x72], [0x3C, 0xBE, 0x0B, 0x38]]
    result3 = keyExpansion(key2, 9)
    assert result3 == [[0xFD, 0x0D, 0x42, 0xCB], [0x0E, 0x16, 0xE0, 0x1C], [0xC5, 0xD5, 0x4A, 0x6E], [0xF9, 0x6B, 0x41, 0x56]]
