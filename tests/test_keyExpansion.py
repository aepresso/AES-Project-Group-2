from aes.keyExpansion import keyExpansion

"""
Tests for keyExpansion function.

To run: python -m pytest tests/test_keyExpansion.py -v
"""

# FIPS-197 Appendix A.1 key (column-major matrix form)
# Raw key bytes: 2b 7e 15 16 28 ae d2 a6 ab f7 15 88 09 cf 4f 3c
KEY_FIPS = [
    [0x2B, 0x28, 0xAB, 0x09],
    [0x7E, 0xAE, 0xF7, 0xCF],
    [0x15, 0xD2, 0x15, 0x4F],
    [0x16, 0xA6, 0x88, 0x3C]
]

KEY_2 = [
    [0xEA, 0xD2, 0x73, 0x21],
    [0xB5, 0x8D, 0xBA, 0xD2],
    [0x31, 0x2B, 0xF5, 0x60],
    [0x7F, 0x8D, 0x29, 0x2F]
]

KEY_ZEROS = [[0x00] * 4 for _ in range(4)]


def test_fips_round1():
    """Round 1 of FIPS-197 key — verified against spec."""
    result = keyExpansion(KEY_FIPS, 1)
    assert result == [
        [0xA0, 0x88, 0x23, 0x2A],
        [0xFA, 0x54, 0xA3, 0x6C],
        [0xFE, 0x2C, 0x39, 0x76],
        [0x17, 0xB1, 0x39, 0x05]
    ]


def test_fips_round2():
    """Round 2 of FIPS-197 key — verified against spec."""
    result = keyExpansion(KEY_FIPS, 2)
    assert result == [
        [0xF2, 0x7A, 0x59, 0x73],
        [0xC2, 0x96, 0x35, 0x59],
        [0x95, 0xB9, 0x80, 0xF6],
        [0xF2, 0x43, 0x7A, 0x7F]
    ]


def test_fips_round5():
    """Round 5 of FIPS-197 key."""
    result = keyExpansion(KEY_FIPS, 5)
    assert result == [
        [0xD4, 0x7C, 0xCA, 0x11],
        [0xD1, 0x83, 0xF2, 0xF9],
        [0xC6, 0x9D, 0xB8, 0x15],
        [0xF8, 0x87, 0xBC, 0xBC]
    ]


def test_fips_round9():
    """Round 9 of FIPS-197 key."""
    result = keyExpansion(KEY_FIPS, 9)
    assert result == [
        [0xAC, 0x19, 0x28, 0x57],
        [0x77, 0xFA, 0xD1, 0x5C],
        [0x66, 0xDC, 0x29, 0x00],
        [0xF3, 0x21, 0x41, 0x6E]
    ]


def test_key2_round1():
    """Round 1 of key2."""
    result = keyExpansion(KEY_2, 1)
    assert result == [
        [0x5E, 0x8C, 0xFF, 0xDE],
        [0x65, 0xE8, 0x52, 0x80],
        [0x24, 0x0F, 0xFA, 0x9A],
        [0x82, 0x0F, 0x26, 0x09]
    ]


def test_key2_round5():
    """Round 5 of key2."""
    result = keyExpansion(KEY_2, 5)
    assert result == [
        [0xD3, 0x68, 0x2D, 0xAA],
        [0xC3, 0xA8, 0x9A, 0x16],
        [0x1F, 0x60, 0xC8, 0xFD],
        [0x46, 0xF3, 0x14, 0x1E]
    ]


def test_key2_round9():
    """Round 9 of key2."""
    result = keyExpansion(KEY_2, 9)
    assert result == [
        [0x68, 0x62, 0xDC, 0x8D],
        [0x8E, 0x50, 0x84, 0xE9],
        [0x5B, 0x1E, 0x83, 0xFE],
        [0xDD, 0x1B, 0x1A, 0xCF]
    ]


def test_zero_key_round1():
    """Round 1 of all-zero key — edge case."""
    result = keyExpansion(KEY_ZEROS, 1)
    assert result == [
        [0x62, 0x62, 0x62, 0x62],
        [0x63, 0x63, 0x63, 0x63],
        [0x63, 0x63, 0x63, 0x63],
        [0x63, 0x63, 0x63, 0x63]
    ]


def test_zero_key_round9():
    """Round 9 of all-zero key — edge case."""
    result = keyExpansion(KEY_ZEROS, 9)
    assert result == [
        [0xB1, 0x8A, 0x1D, 0x4C],
        [0xD4, 0x7D, 0x7B, 0x66],
        [0xD8, 0xB9, 0xB3, 0x49],
        [0xE2, 0xDA, 0xDE, 0x41]
    ]


def test_output_is_4x4():
    """Return value is always a 4x4 matrix."""
    result = keyExpansion(KEY_FIPS, 3)
    assert len(result) == 4
    for row in result:
        assert len(row) == 4


def test_output_bytes_in_range():
    """All output bytes are valid (0x00–0xFF)."""
    for r in range(1, 10):
        result = keyExpansion(KEY_FIPS, r)
        for row in result:
            for byte in row:
                assert 0x00 <= byte <= 0xFF